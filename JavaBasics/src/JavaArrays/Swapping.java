package JavaArrays;
// Swapping with 3 variables
public class Swapping {

	public static void main(String[] args) {
     int[] arr = new int[4];
     
     int a=10;
	 int b = 20;
     int temp =0;
	   System.out.println(a);
	   System.out.println(b);
	   System.out.println("Before Swapping");
	   temp=a;
	   a=b;
	   b=temp;
	  
	   System.out.println("After Swapping");
	   System.out.println(a);
	   System.out.println(b);
	}

}
